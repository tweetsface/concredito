     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $promotor=new Promotor();
        $promotor->nombre_promotor=$request->nombre_promotor;
        $promotor->primer_apellido=$request->primer_apellido;
        $promotor->segundo_apellido=$request->segundo_apellido;
        $promotor->email=$request->email;
        $promotor->password=bcrypt($request->password);
        $promotor->api_token=Str::random(60);
        $promotor->save();
        return Response::json([$promotor,201]);
    }

